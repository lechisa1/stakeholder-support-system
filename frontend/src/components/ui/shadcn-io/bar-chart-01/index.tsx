const chartConfig = {
  views: {
    label: "Page Views",
  },
  desktop: {
    label: "Desktop",
    color: "hsl(var(--chart-1))",
  },
  mobile: {
    label: "Mobile",
    color: "hsl(var(--chart-2))",
  },
} satisfies ChartConfig;

type ChartDataKey = keyof Pick<(typeof chartData)[0], "desktop" | "mobile">;

export function ChartBarInteractive() {
  const [activeChart, setActiveChart] = React.useState<ChartDataKey>("desktop");

  const total = React.useMemo(
    () => ({
      desktop: chartData.reduce((acc, curr) => acc + curr.desktop, 0),
      mobile: chartData.reduce((acc, curr) => acc + curr.mobile, 0),
    }),
    []
  );

  return (
    <Card className="hover:shadow-lg transition-all duration-300 w-full h-full flex flex-col p-6">
      <div className="flex flex-col items-stretch border-b pb-4 mb-4 sm:flex-row">
        <div className="flex flex-1 flex-col justify-center gap-1">
          <h3 className="text-lg font-semibold">Bar Chart - Interactive</h3>
          <p className="text-sm text-muted-foreground">
            Showing total visitors for the last 30 days
          </p>
        </div>
        <Select
          value={activeChart}
          onValueChange={(val) => setActiveChart(val as ChartDataKey)}
        >
          <SelectTrigger className="w-[180px] rounded-lg">
            <SelectValue placeholder="Select Chart" />
          </SelectTrigger>

          <SelectContent className="rounded-xl">
            {Object.keys(chartConfig)
              .filter((key) => key !== "views") // Filter out 'views' from the select options
              .map((key) => {
                const chart = key as ChartDataKey;

                return (
                  <SelectItem
                    key={chart}
                    value={chart}
                    className="rounded-lg flex justify-between"
                  >
                    <div className="flex items-center space-x-2">
                      <span className="text-muted-foreground text-xs">
                        {chartConfig[chart].label}:
                      </span>
                      <span className="font-bold text-sm">
                        {total[chart].toLocaleString()}
                      </span>
                    </div>
                  </SelectItem>
                );
              })}
          </SelectContent>
        </Select>
      </div>
      <div className="flex-1 flex items-center justify-center min-h-0">
        <ChartContainer config={chartConfig} className="h-[250px] w-full">
          <BarChart
            accessibilityLayer
            data={chartData}
            margin={{
              left: 12,
              right: 12,
            }}
          >
            <CartesianGrid vertical={false} />
            <XAxis
              dataKey="date"
              tickLine={false}
              axisLine={false}
              tickMargin={8}
              minTickGap={32}
              tickFormatter={(value) => {
                const date = new Date(value);
                return date.toLocaleDateString("en-US", {
                  month: "short",
                  day: "numeric",
                });
              }}
            />
            <ChartTooltip
              content={
                <ChartTooltipContent
                  className="w-[150px]"
                  nameKey="views"
                  labelFormatter={(value) => {
                    return new Date(value).toLocaleDateString("en-US", {
                      month: "short",
                      day: "numeric",
                      year: "numeric",
                    });
                  }}
                />
              }
            />
            <Bar dataKey={activeChart} fill={`var(--color-${activeChart})`} />
          </BarChart>
        </ChartContainer>
      </div>
    </Card>
  );
}
